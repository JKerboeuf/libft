#include "libft.h"

void	*ft_memchr(void const *s, int c, size_t n)
{
	unsigned char	*buff;

	buff = (unsigned char*)s;
	while (n)
	{
		if (*buff == (unsigned char)c)
			return (buff);
		buff++;
		n--;
	}
	return (NULL);
}
